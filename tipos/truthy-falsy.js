// Truthy e Falsy

// São dois valores relacionados ao True e False

 // 0 -> false ( O número (0) é igual a false, tem o mesmo valor )
 // 1 -> True  ( O número (1) é igual a true, tem o mesmo valor )

 // (==) Compara duas variáveis independente do tipo.
// (===) Compara duas variáveis em tipo e valor.

 console.log(0 == false);
 console.log("" == false); // Uma String vazia ("") também é entendida como false.
 console.log(1 == true);